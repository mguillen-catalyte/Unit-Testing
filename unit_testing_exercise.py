num_list = [1, 9, 10, 15, 22]


def sum_of_numbers(num_list):
    """
    Finds the sum of all the numbers on the given list
    """
    return sum(num_list)


def minimum_number(num_list):
    """
    Finds the minumum number on the list
    """
    return min(num_list)


def maximum_number(num_list):
    """
    Finds the maximum number on the list
    """
    return max(num_list)


def average_number(num_list):
    """
    Finds the average number from the list.
    """
    return sum(num_list) / len(num_list)
